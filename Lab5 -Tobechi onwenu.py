import turtle
d_t=turtle.Turtle()
#fill start
d_t.pu()
d_t.setpos(-500,200)
d_t.pd()
d_t.shape("arrow")
d_t.color("orange2")
d_t.begin_fill()
d_t.fd(900)
d_t.rt(90)
d_t.fd(550)
d_t.rt(90)
d_t.fd(900)
d_t.rt(90)
d_t.fd(550)
d_t.end_fill()
d_t.pu()
#fill end
d_t.setpos(-200,50)
d_t.pd()
d_t.shape("circle")
d_t.width(40)
d_t.color('blue')
d_t.circle(100)
#copy
d_t.pu()
d_t.setpos(35,50)
d_t.pd()
d_t.width(40)
d_t.color('black')
d_t.circle(100)
#paste
d_t.pu()
d_t.setpos(270,50)
d_t.pd()
d_t.width(40)
d_t.color('red')
d_t.circle(100)
d_t.pu()
d_t.setpos(-84,-50)
d_t.pd()
d_t.width(40)
d_t.color('yellow')
d_t.circle(100)
d_t.pu()
d_t.setpos(150,-50)
d_t.pd()
d_t.width(40)
d_t.color('green')
d_t.circle(100)
#
d_t.pu()
d_t.setpos(-220,-200)
d_t.pd()
d_t.shape('turtle')
d_t.st()
d_t.width(20)
d_t.color('red')
d_t.right(180)
d_t.fd(85)
d_t.circle(40,180)
d_t.fd(85)
d_t.pu()
d_t.setpos(-40,-200)
d_t.pd()
d_t.lt(90)
d_t.fd(40)
d_t.circle(30,180)
d_t.fd(20)
d_t.circle(-30,180)
d_t.fd(48)
d_t.pu()
d_t.setpos(50,-200)
d_t.pd()
d_t.lt(65)
d_t.fd(130)
d_t.pu()
d_t.setpos(50,-200)
d_t.pd()
d_t.lt(50)
d_t.fd(130)
d_t.pu()
d_t.setpos(30,-270)
d_t.pd()
d_t.lt(67)
d_t.fd(60)
d_t.pu()
d_t.setpos(150,-320)
d_t.pd()
d_t.stamp()
